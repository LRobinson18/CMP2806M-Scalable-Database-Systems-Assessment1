CREATE PROCEDURE `GetDeliveries`(IN `EID` INT(11), IN `CDATE` DATE)
BEGIN
	SELECT EmployeeID, COUNT(*) as Deliveries, CurrentDate
	FROM tbldeliveries
	INNER JOIN tblEmployees USING(EmployeeID)
	WHERE EmployeeID = EID AND CurrentDate = CDATE;
END//

