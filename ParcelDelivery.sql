CREATE PROCEDURE `GetDriverLocation`(IN `EID` INT(11), IN `CDATE` DATE, IN `CTIME` VARCHAR(12))
BEGIN
	SELECT EmployeeID, CurrentDate, LocationEnd, TimeDelivered
	FROM tbldeliveries
	WHERE EmployeeID = EID AND CurrentDate = CDATE AND TimeDelivered = CONVERT(CTIME, TIME);
END//

